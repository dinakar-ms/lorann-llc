/* ── Run ──────────────────────────────────────────────── */
async function main() {
  console.log("🗑  Deleting all existing dataCard documents...");

  // Fetch all existing dataCard IDs
  const existing = await client.fetch(`*[_type == "dataCard"]._id`);
  console.log(`   Found ${existing.length} existing cards to delete.`);

  if (existing.length > 0) {
    const tx = client.transaction();
    for (const id of existing) {
      tx.delete(id);
    }
    await tx.commit();
    console.log(`   ✓ Deleted ${existing.length} cards.`);
  }

  console.log("\n📝 Seeding 15 Lorann data cards...\n");

  const tx = client.transaction();
  const today = new Date().toISOString().split("T")[0]; // YYYY-MM-DD

  for (const card of cards) {
    const doc = {
      _type: "dataCard",
      name: card.name,
      universe: card.universe,
      lastUpdated: today,
      category: card.category,
      description: card.description,
      postalRecords: card.postalRecords,
      phoneNumbers: card.phoneNumbers,
      emailAddresses: card.emailAddresses,
      postalCpm: card.postalCpm,
      phoneCpm: card.phoneCpm,
      emailCpm: card.emailCpm,
      popularity: card.popularity,
      cardQuality: card.cardQuality,
      market: card.market,
      dataType: card.dataType,
      source: card.source,
      geo: card.geo,
      genderMale: card.genderMale,
      genderFemale: card.genderFemale,
      selects: card.selects,
      minimumOrder: card.minimumOrder,
      minimumPrice: card.minimumPrice,
      netNamePercent: card.netNamePercent,
      brokerCommission: card.brokerCommission,
      agencyCommission: card.agencyCommission,
      exchangeAvailable: card.exchangeAvailable,
      reuseAvailable: card.reuseAvailable,
      emailDeliveryFee: card.emailDeliveryFee,
      ftpDeliveryFee: card.ftpDeliveryFee,
      marketEntryDate: "2024-03-15",
      nextUpdateDate: "2026-07-01",
      frequency: card.frequency,
    };
    tx.create(doc);
    console.log(`   + ${card.name} (${new Intl.NumberFormat("en-US").format(card.universe)} records)`);
  }

  const result = await tx.commit();
  console.log(`\n✅ Done! Seeded 15 Lorann data cards. Transaction: ${result.transactionId}`);
}

main().catch((err) => {
  console.error("❌ Error:", err.message);
  process.exit(1);
});
